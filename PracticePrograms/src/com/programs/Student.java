package com.programs;

public class Student {
	private String name;
	private int roll;
	private int marks;
	
	public void setName(String name) {
		this.name=name;
		
	}
	
	public String getName() {
		return name;
		
	}
	
	public void setRoll(int roll) {
		this.roll=roll;
	
	}
	
	public int getRoll() {
		return roll;
	}
	
	public void setMarks(int marks) {
		this.marks=marks;
		
	}
	
	public int getMarks() {
		return marks;
	}
	

}
